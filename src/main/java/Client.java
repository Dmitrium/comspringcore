
public class Client {
    String id;
    String fullname;

    public Client(String s, String s1) {
        id = s;
        fullname = s1;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    
}
